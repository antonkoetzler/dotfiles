config.hide_tab_bar_if_only_one_tab = false
config.tab_max_width                = 32

-- ─── Keybindings ───────────────────────────────────────────────────────────────────────────
--
--  All platforms
--    Ctrl+T                  new tab (default shell)
--    Ctrl+Left / Ctrl+Right  prev / next tab
--    Ctrl+Shift+Left/Right   reorder (move) tab
--    Alt+1..9                jump to tab by position
--
--  Windows only
--    Ctrl+1  open Git Bash tab
--    Ctrl+2  open PowerShell tab
--    Ctrl+3  open CMD tab
--
local keys = {
  { key = 't',          mods = 'CTRL',       action = act.SpawnTab 'CurrentPaneDomain' },
  { key = 'w',          mods = 'CTRL',       action = act.CloseCurrentTab { confirm = false } },
  { key = 'LeftArrow',  mods = 'CTRL',       action = act.ActivateTabRelative(-1) },
  { key = 'RightArrow', mods = 'CTRL',       action = act.ActivateTabRelative(1) },
  { key = 'LeftArrow',  mods = 'CTRL|SHIFT', action = act.MoveTabRelative(-1) },
  { key = 'RightArrow', mods = 'CTRL|SHIFT', action = act.MoveTabRelative(1) },
  { key = '1', mods = 'ALT', action = act.ActivateTab(0) },
  { key = '2', mods = 'ALT', action = act.ActivateTab(1) },
  { key = '3', mods = 'ALT', action = act.ActivateTab(2) },
  { key = '4', mods = 'ALT', action = act.ActivateTab(3) },
  { key = '5', mods = 'ALT', action = act.ActivateTab(4) },
  { key = '6', mods = 'ALT', action = act.ActivateTab(5) },
  { key = '7', mods = 'ALT', action = act.ActivateTab(6) },
  { key = '8', mods = 'ALT', action = act.ActivateTab(7) },
  { key = '9', mods = 'ALT', action = act.ActivateTab(8) },
}

if is_windows then
  local win_keys = {
    { key = '1', mods = 'CTRL', action = act.SpawnCommandInNewTab { args = GIT_BASH } },
    { key = '2', mods = 'CTRL', action = act.SpawnCommandInNewTab { args = POWERSHELL } },
    { key = '3', mods = 'CTRL', action = act.SpawnCommandInNewTab { args = CMD } },
  }
  for _, k in ipairs(win_keys) do table.insert(keys, k) end
end

config.keys = keys

return config
